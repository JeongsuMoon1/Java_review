package org.opentutorials.javatutorials._7_operator;

public class _3_RemainderDemo {

	public static void main(String[] args) {
		
		// 나머지 연산자(remainder)
		
		int a = 3;
		System.out.println(0%a); // 나머지 0
		System.out.println(1%a); // 나머지 1
		System.out.println(2%a); // 나머지 2
		System.out.println(3%a); // 나머지 0
		System.out.println(4%a); // 나머지 1
		System.out.println(5%a); // 나머지 2
		System.out.println(6%a); // 나머지 0

	}

}
