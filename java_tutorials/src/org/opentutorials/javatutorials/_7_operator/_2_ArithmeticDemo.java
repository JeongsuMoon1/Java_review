package org.opentutorials.javatutorials._7_operator;

public class _2_ArithmeticDemo {

	public static void main(String[] args) {
		
		// 산술 연산자
		// +(더하기), -(빼기), *(곱하기,에스터리스크), /(나누기), %(나머지)
		
		// result의 값은 3
		int result = 1+2;
		System.out.println(result);
		
		// result의 값은 2
		result = result -1;
		System.out.println(result);
		
		// result의 값은 4
		result = result * 2;
		System.out.println(result);
		
		// result의 값은 2
		result = result / 2;
		System.out.println(result);
		
		// result의 값은 10
		result = result + 8;
		// result의 값은 3
		result = result % 7;
		System.out.println(result);

	}

}
