package org.opentutorials.javatutorials._930_array;

public class _2_GetDemo {

	public static void main(String[] args) {
		
		// 03.배열의 제어 
				// 변수에 담긴 데이터 중에서 특정 데이터를 가져오고 싶을 경우.
				// 배열에 담긴 데이터에 접근
				String[] ClassGroup = {"최진혁","최유빈","한이람","이고잉"	};	// 배열을 한 번에 정의하는 방법이다.
				System.out.println(ClassGroup[0]);
				System.out.println(ClassGroup[1]);
				System.out.println(ClassGroup[2]);
				System.out.println(ClassGroup[3]);
				// index(인덱스) : 0,1,2,3 값(한국어로 색인)
				// 컴퓨터의 숫자 체계는 0부터 시작한다.
				
	}

}
