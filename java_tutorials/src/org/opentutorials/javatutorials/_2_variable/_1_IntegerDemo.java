package org.opentutorials.javatutorials._2_variable;

public class _1_IntegerDemo {

	public static void main(String[] args) {
		
		// 변수를 만들고 사용할 떄는 선언(declare)와 할당(assign)이라 한다.
		// 변수 선언 : [데이터 형식 + 변수의 이름]으로 두 가지 요소로 구성돼있다.
		// int : 데이터 형식, integer(숫자)의 줄임말.
		// a : 변수의 이름
		
		
		int a; // int라는 것은 a라는 변수에 담을 수 있는 값이 숫자이다.
		a = 1;
		System.out.println(a+1); //2
		a = 2;
		System.out.println(a+1); //3
				
	}

}
