package org.opentutorials.javatutorials._2_variable;

public class _3_StringDemo {

	public static void main(String[] args) {
		
		// 문자열 데이터 형식
		// String 대신 int, double을 사용하면 오류가 발생
		
		
		// 간략화
		String first = "coding";
		System.out.println(first+" "+"everybody");
		
		// 풀어씀
		String a, b;
		a = "coding";
		b = " everybody";
		System.out.println(a+b);
		
	}

}
