package org.opentutorials.javatutorials._2_variable;

public class _2_DoubleDemo {

	public static void main(String[] args) {
		
		// 정수와 실수
		// 정수(int): 소수점 없음 
		// 실수(double): 소수점 있음
				
		double a = 1.1; // 선언부
		System.out.println(a+1.1); // 2.2
		a = 2.1;
		System.out.println(a+1.1); // 3.2
		
		// 오류 소스코드 
//		int a;
//		a = 1.1;
		
	}

}
