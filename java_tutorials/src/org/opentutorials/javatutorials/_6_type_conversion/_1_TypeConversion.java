package org.opentutorials.javatutorials._6_type_conversion;

public class _1_TypeConversion {

	public static void main(String[] args) {
		
		// 형 변환(type conversion) : 어떤 데이터 타입을 변환 한다는 뜻.
		// int 데이터 타입을 double이나 float으로 바꾸고 싶을 경우
		
		
		// int 200의 비트값 : 00000000 00000000 00000000 11001000
		// (double,float) 200.0의 비트값 : 01000011 00000000 00000000 00000000 
		// 컴퓨터 입장에서는 정수와 실수는 전혀 다른 값이다.
		
		// 서로 다른 데이터 타입의 데이터를 더하거나 연산할 경우는 데이터 타입을 다른 쪽의 데이터 타입으로 맞춰야 한다 = 형 변환(type conversion)
		
		// 자바는 형 변환(type conversion)을 자동으로 해주기도 함 : 암시적 형 변환, 자동 형 변환(Implicit conversion)
		
		
		
	}

}
